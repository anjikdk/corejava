package oops;

public class Calculator {

	public void add(int i, int j)
	{
		int k = i+j;
		System.out.println(k);
	}
	
	public void sub(int i, int j)
	{
		int k = i-j;
		System.out.println(k);
	}
	
	public void multi(int i, int j)
	{
		int k = i*j;
		System.out.println(k);
	}
	
	public void div(int i, int j)
	{
		int k = i/j;
		System.out.println(k);
	}
}
